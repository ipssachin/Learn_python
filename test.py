#number and Math functions

# Division always return float value

x = 10 / 2
print(x)
print(type(x))

# floor division discards the fractional part

x = 10//2
print(x)
print(type(x))

# the % operator returns the remainder of the division (MOD)
x=15%7
print(x)
print(type(x))

#use the ** operator to calculate powers

x = 5**2

print(x)
print(type(x))

x = 2 ** 7 # 2 raised to the power of 7

print(type(x))
print(x)

#equal sign (=) is used to assign a value to a variable.

width = 20
height = 5 * 9
